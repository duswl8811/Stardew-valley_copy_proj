	g_hWnd = CreateWindowW(szWindowClass, (L"Stardew valley"), WS_OVERLAPPEDWINDOW,
		100, 50, WINDOW_SIZE_X, WINDOW_SIZE_Y, nullptr, nullptr, hInstance, nullptr);

